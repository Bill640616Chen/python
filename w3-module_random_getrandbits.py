#Random getrandbits() Method getrandbits()方法
#傳回表示隨機位的數位。
#Return an 8 bits sized integer:
#傳回一個 8 位元大小的整數：
import random
print(random.getrandbits(8))
#Definition and Usage 定義和用法
#The method returns an integer in the specified size (in bits).getrandbits()
#該方法返回指定大小（以位為單位）的整數。getrandbits()
#Syntax 語法
#random.getrandbits(n)
#Parameter Values 參數值
#Parameter	Description
#n：Required. A number specifying the size, in bits, of the returned integer.
#必填。一個數位，指定返回的整數的大小（以位元為單位）。
#不能是負數

