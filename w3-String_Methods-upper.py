#Python 字串 upper() Method
#Upper case the string:
#字串都大寫
txt = "Hello my friends"
x = txt.upper()
print(x) #HELLO MY FRIENDS
#定義和使用
#The method returns a string where all characters are in upper case.upper()
#Symbols and Numbers are ignored.符號跟數字可以忽略
#Syntax語法
#string.upper()
#Parameter Values 參數值
#No parameters 無參數
point = {'x':4,'y':-5}
print('{x} '.format(**point))
#用format只能接受字串型的鍵,取鍵的值
