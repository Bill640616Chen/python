#while_continue
#The continue Statement
#With the continue statement we can stop the current iteration, and continue with the next:
#通過繼續聲明，我們可以停止當前反覆運算，並繼續下一次：
#Continue to the next iteration if i is 3:
#如果ｉ是 3：請繼續下一次反覆運算：
i = 0
while i < 6:
  i += 1
  if i == 3:
    continue
  print(i)
