#NumPy Introduction NumPy簡介
#What is NumPy?什麼是 NumPy？
#NumPy is a Python library used for working with arrays.
#NumPy 是用於與陣列配合工作的 python 庫。
#It also has functions for working in domain of linear algebra, fourier transform, and matrices.
#它還擁有在線性代數、傅立葉變換和矩陣領域中工作的函數。
#NumPy was created in 2005 by Travis Oliphant. It is an open source project and you can use it freely.
#NumPy 由 Travis Oliphant 於 2005 年創建。 它是一個開源專案，您可以自由使用它。
#NumPy stands for Numerical Python.
#NumPy 指的是數值 Python（Numerical Python）。
#Why Use NumPy?為何使用 NumPy？
#In Python we have lists that serve the purpose of arrays, but they are slow to process.
#在 Python 中，我們有滿足數位功能的清單，但是處理起來很慢。
#NumPy aims to provide an array object that is up to 50x faster than traditional Python lists.
#NumPy 旨在提供一個比傳統 Python 清單快 50 倍的數位物件。
#The array object in NumPy is called ndarray, it provides a lot of supporting functions that make working with ndarray very easy.
#NumPy 中的陣列物件稱為 ndarray，它提供了許多支援函數，使得利用 ndarray 非常容易。
#Arrays are very frequently used in data science, where speed and resources are very important.
#陳列在數據科學中非常常用，因為速度和資源非常重要。
#Data Science: is a branch of computer science where we study how to store, use and analyze data for deriving information from it.
#數據科學：計算機科學的一個分支，研究如何存儲、使用和分析數據以從中獲取資訊。
#Why is NumPy Faster Than Lists? 為什麼 NumPy 比清單快？
#NumPy arrays are stored at one continuous place in memory unlike lists, so processes can access and manipulate them very efficiently.
#與清單不同，NumPy 陣組存儲在記憶體中的一個連續位置，因此進程可以非常有效地訪問和操縱它們。
#This behavior is called locality of reference in computer science.
#這種行為在計算機科學中稱為引用的局部性。
#This is the main reason why NumPy is faster than lists. Also it is optimized to work with latest CPU architectures.
#這是 NumPy 比清單更快的主要原因。 它還經過了優化，可與最新的 CPU 體系結構一同使用。
#Which Language is NumPy written in? NumPy 用哪種語言編寫？
#NumPy is a Python library and is written partially in Python, but most of the parts that require fast computation are written in C or C++.
#NumPy 是一個 Python 庫，部分用 Python 編寫，但是大多數需要快速計算的部分都是用 C 或 C ++ 編寫的。
#Where is the NumPy Codebase? NumPy 代碼庫在哪裡？
#The source code for NumPy is located at this github repository https://github.com/numpy/numpy
#NumPy 的原始碼位於這個 github 資料庫中：https://github.com/numpy/numpy
#github: enables many people to work on the same codebase.
#github：使許多人可以在同一代碼庫上工作。