#Python divmod Function Python divmod函數
#Returns the quotient and the remainder when argument1 is divided by argument2
#當參數1除以參數2時，返回商和餘數。
#Display the quotient and the remainder of 5 divided by 2:
#顯示 5 除以 2 的商和餘數：
x = divmod(5, 2)
print(x)
#Definition and Usage 定義和用法
#The divmod() function returns a tuple containing the quotient  and the remainder when argument1 (dividend) is divided by argument2 (divisor).
#divmod（） 函數傳回當參數 1 除以參數 2 時包含商和餘數的元組。
#Syntax 語法
#divmod(dividend, divisor)
#Parameter參數：Values值
#Parameter參數：Description描述
#dividend：A Number. The number you want to divide
#dividend：數位。 被除數。
#divisor：A Number. The number you want to divide with
#divisor：數位。 除數。