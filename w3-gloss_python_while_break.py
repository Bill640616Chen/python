#With the break statement we can stop the loop even if the while condition is true:
#與中斷語句，我們可以停止迴圈，即使條件是真實的：
#Exit the loop when i is 3:
#當i是3時,跳出迴圈
i = 1
while i < 6:
  print(i)
  if i == 3:
    break
  i += 1
