#Nested Loops
#A nested loop is a loop inside a loop.
#嵌套循環是迴圈內的環。
#The "inner loop" will be executed one time for each iteration of the "outer loop":
#"內迴圈"將執行一次"外迴圈"的每個反覆運算：
#Print each adjective for every fruit:
#列印每個水果的每個形容詞：
adj = ["red", "big", "tasty"]
fruits = ["apple", "banana", "cherry"]
for x in adj:
  for y in fruits:
    print(x, y)
