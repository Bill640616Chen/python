#Loop Iterator 迴圈迭代
#We can also use a loop to iterate through an iterable object:for
#我們還可以使用循環通過可轉重對象進行迴圈：for
#Iterate the values of a tuple:
#迭代一個tuple的值：
mytuple = ("apple", "banana", "cherry")
for x in mytuple:
  print(x)
#Iterate the characters of a string:
#迭代字串的字元
mystr = "banana"
for x in mystr:
  print(x)
#The loop actually creates an iterator object and executes the next() method for each loop.for
#環實際上創建一個轉子物件，併為每個循環執行下一個 （） 方法。for
