#Assignment operators are used to assign values to variables:
#配置運算子用於為變數分配值：
#Operator	Example	    Same As	
#=	        x = 5	    x = 5	
#+=	        x += 3	    x = x + 3	
#-=	        x -= 3	    x = x - 3	
#*=	        x *= 3	    x = x * 3	
#/=	        x /= 3	    x = x / 3	
#%=	        x %= 3	    x = x % 3	
#//=	    x //= 3	    x = x // 3	
#**=	    x **= 3	    x = x ** 3	
#&=	        x &= 3	    x = x & 3	
##|=	    x |= 3	    x = x | 3	
#^=	        x ^= 3	    x = x ^ 3	
#>>=	    x >>= 3	    x = x >> 3	
#<<=	    x <<= 3	    x = x << 3
