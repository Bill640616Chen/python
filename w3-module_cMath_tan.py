#Python cmath.tan() Method
#Find the tangent of a complex number:
#找到複數的切線：
#Import cmath Library
import cmath
#Return the tangent of a complex number
print (cmath.tan(2 + 3j))
#Definition and Usage 定義和用法
#The method returns the tangent of a complex number.cmath.tan()
#該方法返回複數的切線。cmath.tan()
#Syntax 語法
#cmath.tan(x)
#Parameter Values 參數值
#Parameter	Description
#x：Required. A number to find the tangent of. If the value is not a number, it returns a TypeError
#必填。要查找切線的數位。如果該值不是數位，則返回 TypeError
