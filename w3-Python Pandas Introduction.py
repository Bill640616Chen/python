#What is Pandas? 什麼是Pandas
#Pandas is a Python library used for working with data sets.
#Pandas是用於處理數據集的 Python 庫。
#It has functions for analyzing, cleaning, exploring, and manipulating data.
#它具有分析、清潔、探索和操縱數據的功能。
