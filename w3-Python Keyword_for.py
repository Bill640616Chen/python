#Python Keyword for 關鍵字 for
#To create a for loop
#創建 for 迴圈。
#Print each number from 1 to 8:
#列印 1 到 8 的每個數位：
for x in range(1, 9):
  print(x)
#Definition and Usage 列印 1 到 8 的每個數位：
#The for keyword is used to create a for loop.
#for 關鍵字用於創建 for 迴圈。
#It can be used to iterate through a sequence, like a list, tuple, etc.
#它可以用來遍歷序列，例如清單，元組等。
#Loop through all items in a list:
print('-------------分隔線------------')
#走過清單中的每個項目：
fruits = ["apple", "banana", "cherry"]
for x in fruits:
  print(x)
