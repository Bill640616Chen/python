#Python type_conversion 類型轉換
#You can convert from one type to another with the , , and methods:int()float()complex()
#您可以根據方法從一種類型轉換為另一種類型：int()float()complex()
#Convert from one type to another:
#從一種類型轉換為另一種類型：
x = 1 # int
y = 2.8 # float
z = 1j # complex
#convert from int to float:
a = float(x)
#convert from float to int:
b = int(y)
#convert from int to complex:
c = complex(x)
print(a)
print(b)
print(c)
print(type(a))
print(type(b))
print(type(c))
#Note: You cannot convert complex numbers into another number type.
#注意：您不能將複數轉換為其他數字類型。
