#Python Floats 浮點
#Float, or "floating point number" is a number, positive or negative, containing one or more decimals.
#浮點，或「浮動點號」是一個數位，正數或負數，包含一個或多個小數。
#Floats:
#浮點
x = 1.10
y = 1.0
z = -35.59
print(type(x))
print(type(y))
print(type(z))
#Float can also be scientific numbers with an "e" to indicate the power of 10.
#Floats:
#浮點
x = 35e3
y = 12E4
z = -87.7e100
print(type(x))
print(type(y))
print(type(z))
