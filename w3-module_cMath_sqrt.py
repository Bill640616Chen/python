#Python cmath.sqrt() Method
#Find the square root of a complex number:
#找到複數的平方根：
#Import cmath Library
import cmath
#Return the square root of a complex number
print (cmath.sqrt(2 + 3j))
print (cmath.sqrt(15))
#Definition and Usage 定義和用法
#The method returns the square root of a complex number.cmath.sqrt()
#該方法返回複數的平方根。cmath.sqrt()
#Note: The number must be greater than or equal to 0.
#注意：該數字必須大於或等於 0。
#Syntax 語法
#cmath.sqrt(x)
#Parameter Values 參數值
#Parameter	Description
#x：Required. A number to find the square root of. If the number is less than 0, it returns a ValueError. If the value is not a number, it returns a TypeError
#必需。要查找平方根的數位。如果該數位小於 0，則返回值錯誤。如果該值不是數位，則返回 TypeError
#Technical Details 技術細節
#Return Value:	A value, representing the square root of a complex numbercomplex
#返回值：	一個值，表示複數的平方根complex
