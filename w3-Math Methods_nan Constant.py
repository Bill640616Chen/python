#Python Math Constants nan 數學常數nan
#Constant   Description
#math.nan	Returns a floating-point NaN (Not a Number) value
#nan代表Not A Number（不是一個數），它並不等於0
#因為nan不是一個數，所以相關計算都無法得到數位。
#所有涉及nan的操作，返回的都是nan。
#Print the value of nan:
# Import math Library
import math
# Print the value of nan
print (math.nan)
#Definition and Usage
#The math.nan constant returns a floating-point nan (Not a Number) value. This value is not a legal number.
#The nan constant is equivalent to float('nan').
#Syntax
math.nan
#Technical Details
#Return Value:	A float value, nan (Not a Number)
#Python Version:	3.5

