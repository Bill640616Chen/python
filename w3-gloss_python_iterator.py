#Iterator 迭代器
#An iterator is an object that contains a countable number of values.
#傳動器是包含可計數值數的物件。
#An iterator is an object that can be iterated upon, meaning that you can traverse through all the values.
#傳轉器是可以重遍的物件，這意味著您可以穿越所有值。
#Technically, in Python, an iterator is an object which implements the iterator protocol, which consist of the methods and .__iter__()__next__()
#從技術上講，在 Python 中，傳導器是執行傳中器協議的物件，該協定由方法和 。__iter__()__next__()
#Iterate the values of a tuple:
#迭代一個tuple的值：
mytuple = ("apple", "banana", "cherry")
for x in mytuple:
  print(x)
