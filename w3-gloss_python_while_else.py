#The else Statement
#With the else statement we can run a block of code once when the condition no longer is true:
#對於其他語句，當條件不再真實時，我們可以運行一個代碼塊一次：
#Print a message once the condition is false:
#一旦條件錯誤，列印消息：
i = 1
while i < 6:
  print(i)
  i += 1
else:
  print("i is no longer less than 6")
