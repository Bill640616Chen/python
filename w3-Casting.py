#Python Casting
#有時，您可能需要指定變數上的類型。這可以通過鑄造完成。Python 是一種面向對象的語言，因此它使用類來定義數據類型，包括其原始類型。
#因此，使用構造器功能在巨蛇中鑄造：
#int （） - 從整數位面、浮動字面（通過刪除所有小數）或字串字面（提供字串表示整個數位）構建整數
#浮動 （） - 從整數位面、浮動字面或字串字面構建浮子編號（提供字串表示浮子或整數）
#str （） - 從各種資料類型構建字串，包括字串、整數位和浮動字體
x = int(1)   # x will be 1
y = int(2.8) # y will be 2
z = int("3") # z will be 3
print(x,y,z)
x = float(1)     # x will be 1.0
y = float(2.8)   # y will be 2.8
z = float("3")   # z will be 3.0
w = float("4.2") # w will be 4.2
print(x, y, z, w)
x = str("s1") # x will be 's1'
y = str(2)    # y will be '2'
z = str(3.0)  # z will be '3.0'
print(x,y,z)