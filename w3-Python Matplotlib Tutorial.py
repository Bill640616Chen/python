##What is Matplotlib? 什麼是Matplotlib
#Matplotlib is a low level graph plotting library in python that serves as a visualization utility.
#Matplotlib 是一個低級圖形繪圖庫，用作可視化實用工具。
#Matplotlib was created by John D. Hunter.
#馬特普洛特利布是由約翰·亨特創造的。
#Matplotlib is open source and we can use it freely.
#Matplotlib是開源的，我們可以自由使用。
#Matplotlib is mostly written in python, a few segments are written in C, Objective-C and Javascript for Platform compatibility.
#Matplotlib 大多以python書寫，少數部分以 C、Objective-C 和 Javascript 書寫平臺相容性。
#Where is the Matplotlib Codebase? Matplotlib代碼庫在哪裡？
#The source code for Matplotlib is located at this github repository https://github.com/matplotlib/matplotlib
#馬特普洛特利布的原始程式碼位於這個 github 儲存庫 https://github.com/matplotlib/matplotlib
