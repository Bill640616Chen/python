#Python bin() Function Python bin()函數
#Returns the binary version of a number
#返回數的二進位版本。
#Return the binary version of 36:
#返回 36 的二進位版本：
x = bin(36)
print(x)
# The result will always have the prefix 0b
#Definition and Usage 定義和用法
#The bin() function returns the binary version of a specified integer.
#bin（） 函數傳回指定整數的二進位版本。
#The result will always start with the prefix 0b.
#結果將始終以前綴 0b 開頭。
#Syntax 語法
#bin(n)
#Parameter參數：Values值
#Parameter參數：Description描述
#n：Required. An integer
#n 必需。 整數。