#Remove Array Item 刪除陣列項目
#You can use the method to remove an element from the array.pop()
#您可以使用該方法從陣列中刪除元素。pop()
#Delete the second element of the array:cars
#刪除陣列的第二個元素：cars
cars = ["Ford", "Volvo", "BMW"]
cars.pop(1)
print(cars)
#You can also use the method to remove an element from the array.remove()
#您也可以使用該方法從陣列中刪除元素。remove()
#Delete the element that has the value "Volvo":
#刪除具有「Volvo」價值的元素：
cars = ["Ford", "Volvo", "BMW"]
cars.remove("Volvo")
print(cars)
#Note: The list's method only removes the first occurrence of the specified value.remove()
#注意：清單的方法僅刪除指定值的第一次發生。remove()
