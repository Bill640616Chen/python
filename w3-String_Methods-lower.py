#Python 字串 lower() Method
#Converts a string into lower case
#轉換字串至小寫
#Lower case the string:字串的字元全部小寫
txt = "Hello my FRIENDS"
x = txt.lower()
print(x) #hello my friends
#定義和使用
#該方法返回所有字元都是小寫的字元字串。lower()
#符號和數字被忽略。
#Syntax語法
#string.lower()
#無參數值
