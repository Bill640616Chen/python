#Python set() Function Python set()函數
#Returns a new set object
#返回新的集合物件。
#Create a set containing fruit names:
#建立包含水果名稱的集合：
x = set(('apple', 'banana', 'cherry'))
print(x)
#Note: the set list is unordered, so the result will display the items in a random order.
#注意：設置清單未按順序排列，因此結果將隨機顯示專案。
#Definition and Usage 定義和用法
#The set() function creates a set object.
#set（） 函數建立集合物件。
#The items in a set list are unordered, so it will appear in random order.
#集合清單中的專案是無序的，因此它將以隨機順序出現。
#Read more about sets in the chapter Python Sets.
#請在「Python 集合」一章中了解有關集合的更多知識。
#Syntax 語法
#set(iterable)
#Parameter參數：Values值
#Paramete參數：Description描述
#iterable：Required. A sequence, collection or an iterator object
#iterable：必需。 序列、集合或反覆運算器物件。只能一個變數