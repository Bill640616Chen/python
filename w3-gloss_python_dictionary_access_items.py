#Accessing Dictionary Items 訪問字典項目
#You can access the items of a dictionary by referring to its key name, inside square brackets:
#您可以透過參考字典的關鍵名稱（方括弧內）存取字典中的專案：
#Get the value of the "model" key:
#獲取「模型」鍵的價值：
thisdict =	{
  "brand": "Ford",
  "model": "Mustang",
  "year": 1964
}
x = thisdict["model"]
print(x)
#There is also a method called that will give you the same result:get()
#還有一種方法稱為「給您相同的結果」：get()
#Get the value of the "model" key:
#獲取「模型」鍵的價值：
thisdict =	{
  "brand": "Ford",
  "model": "Mustang",
  "year": 1964
}
x = thisdict.get("model")
print(x)
