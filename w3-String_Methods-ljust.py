#Python 字串 lower() Method
#Converts a string into lower case 轉換字申至小寫
#Lower case the string:小寫字申
txt = "Hello my FRIENDS"
x = txt.lower()
print(x) #hello my friends
#定義和使用
#The method returns a string where all characters are lower case.lower()
#返回字申裡所有的字元都小寫
#Symbols and Numbers are ignored.忽略符號和數字
