#Using the dir() Function 使用 dir()函數
#There is a built-in function to list all the function names (or variable names) in a module. The function:dir()
#有一個內置函數可以列出模組中的所有函數名稱（或變數名稱）。該功能：dir()
#List all the defined names belonging to the platform module:
#列出屬於平台模組的所有已定義名稱：
import platform
x = dir(platform)
print(x)
#Note: The dir() function can be used on all modules, also the ones you create yourself.
#注意：dir（） 函數可用於所有模組，也可用於您自己創建的模組。
