#Python Keyword True 關鍵字 True
#Boolean value, result of comparison operations
#布爾值，比較運算的結果。
#Print the result of the comparison "7 is larger than 6":
#列印比較 「7大於6」 的結果：
print(7 > 6)
#Definition and Usage 定義和用法
#The True keyword is a Boolean value, and result of a comparison operation.
#True 關鍵字是一個布爾值，是比較運算的結果。
#The True keyword is the same as 1 (False is the same as 0).
#True 關鍵字與 1 相同（Fals 與 0 相同）。
#Other comparisons that returns True:
#其他返回 True 的比較：
print(5 < 6)
print(2 in [1,2,3])
print(5 is 5)
print(5 == 5)
print(5 == 5 or 6 == 7)
print(5 == 5 and 7 == 7)
print("hello" is not "goodbye")
print(not(5 == 7))
print(4 not in [1,2,3])
