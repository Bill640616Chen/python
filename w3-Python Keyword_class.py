#Python Keyword class 關鍵字 class
#To define a class
#定義類。
#Create a class named "Person":
#建立名為 「Person」 的類別：
class Person:
  name = "John"
  age = 36
#Definition and Usage 定義和用法
#The class keyword is used to create a class.
#class 關鍵字用於創建類。
#A class is like an object constructor. See the example below to see how we can use it to create an object.
#類就像物件構造函數。 請看下面的例子，理解如何使用類創建物件。
#Create an object named p1, using the class from the example above:
#建立物件 p1，使用上例中的類別：
p1 = Person()
print(p1.name)
