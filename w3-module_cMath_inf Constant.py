#Import cmath Library
import cmath
# Print the positive infinity
print (cmath.inf)
# Print the negative infinity
print (-cmath.inf)
#Definition and Usage 定義和用法
#The constant returns a floating-point positive infinity.cmath.inf
#常量返回浮點正無窮大。cmath.inf
#For negative infinity, use .-cmath.inf
#對於負無窮大，請使用 。-cmath.inf
#The cmath.inf constant is equivalent to .float("inf")
#cmath.inf 常量等價於 。float("inf")
