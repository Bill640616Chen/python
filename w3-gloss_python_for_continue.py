#Continue For Loop
#With the continue statement we can stop the current iteration of the loop, and continue with the next:
#通過繼續聲明，我們可以停止迴圈的當前反覆運算，並繼續下一個：
#Do not print banana:
#不要列印香蕉：
fruits = ["apple", "banana", "cherry"]
for x in fruits:
  if x == "banana":
    continue
  print(x)
