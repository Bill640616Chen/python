#Python File isatty() Method Python isatty
#Returns whether the file stream is interactive or not
#返回檔流是否是互動式的。
#Check if the file is connected to a terminal device:
#檢查檔案是否已連接到終端裝置：
f = open("demofile.txt", "r")
print(f.isatty())
#Definition and Usage 定義和用法
#The isatty() method returns True if the file stream is interactive, example: connected to a terminal device.
#如果檔流是互動式的，則 isatty（） 方法返回 True，例如：已連接到終端設備。
#Syntax 語法
#file.isatty()
#Parameter參數：Values值
