#Python Date Time 日期時間
#A date in Python is not a data type of its own, but we can import a module named to work with dates as date objects.datetime
#Python中的日期不是它自己的數據類型，但我們可以導入一個名為將日期用作日期對象的模組。datetime
#Import the datetime module and display the current date:
#匯入日期時間模組並顯示目前日期：
import datetime
x = datetime.datetime.now()
print(x)
