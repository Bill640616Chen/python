#To check if a certain phrase or character is present in a string, we can use the keywords or .innot in
#要檢查某個短語或字元是否存在於字串中，我們可以使用關鍵字或 。innot in
#Check if the phrase "ain" is present in the following text:
#檢查以下文字中是否存在短語「ain」：
txt = "The rain in Spain stays mainly in the plain"
x = "ain" in txt
print(x)
#Check if the phrase "ain" is NOT present in the following text:
#檢查以下文字中是否不存在「ain」短語：
txt = "The rain in Spain stays mainly in the plain"
x = "ain" not in txt
print(x) 
