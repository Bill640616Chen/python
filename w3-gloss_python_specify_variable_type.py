#Python specify_variable_type 指定可變類型
#There may be times when you want to specify a type on to a variable. This can be done with casting. Python is an object-orientated language, and as such it uses classes to define data types, including its primitive types.
#有時，您可能需要指定變數上的類型。這可以通過鑄造完成。Python 是一種面向對象的語言，因此它使用類來定義數據類型，包括其原始類型。
#Casting in python is therefore done using constructor functions:
#因此，使用構造器功能在Casting in python
#int() - constructs an integer number from an integer literal, a float literal (by rounding down to the previous whole number), or a string literal (providing the string represents a whole number)
#int() - 從整數位面、浮動字面（通過四捨五入到上一個完整數位）或字串字面（提供字串表示整個數位）構建整數
#float() - constructs a float number from an integer literal, a float literal or a string literal (providing the string represents a float or an integer)
#浮動() - 從整數位面、浮動字面或字串字面構建浮子編號（提供字串表示浮子或整數）
#str() - constructs a string from a wide variety of data types, including strings, integer literals and float literals
#str() - 從各種資料類型構建字串，包括字串、整數位和浮動字體
#Integers:
#整數：
x = int(1)   # x will be 1
y = int(2.8) # y will be 2
z = int("3") # z will be 3
#Floats:
#浮點：
#x = float(1)     # x will be 1.0
#y = float(2.8)   # y will be 2.8
#z = float("3")   # z will be 3.0
#w = float("4.2") # w will be 4.2
#Strings:
#字串：
#x = str("s1") # x will be 's1'
#y = str(2)    # y will be '2'
#z = str(3.0)  # z will be '3.0'
