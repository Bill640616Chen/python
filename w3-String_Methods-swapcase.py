#Python 字串 swapcase() Method
#Make the lower case letters upper case and the upper case letters lower case:
#使小寫變大寫和大寫變小寫
txt = "Hello My Name Is PETER"
x = txt.swapcase()
print(x)
#hELLO mY nAME iS peter
#定義和使用
#The method returns a string where all the upper case letters are lower case and vice versa.swapcase()
#該方法返回一個字串，其中所有的大寫字母都是小寫字母，反之亦然。
