#Adding Items in a Dictionary 在字典中新增項目
#Adding an item to the dictionary is done by using a new index key and assigning a value to it:
#使用新的索引鍵並分配其值，將項目添加到字典中完成：
thisdict = {
  "brand": "Ford",
  "model": "Mustang",
  "year": 1964
}
thisdict["color"] = "red"
print(thisdict)
