#Python cmath.tanh() Method
#Find the hyperbolic tangent of a complex number:
#求出複數的雙曲正切：
#Import cmath Library
import cmath
#Return the hyperbolic tangent of a complex number
print (cmath.tanh(2 + 3j))
#Definition and Usage 定義和用法
#The method returns the hyperbolic tangent of a complex number.cmath.tanh()
#該方法返回複數的雙曲正切。cmath.tanh()
#Syntax 語法
#cmath.tanh(x)
#Parameter Values 參數值
#Parameter	Description
#x：Required. A number to find the hyperbolic tangent of. If the value is not a number, it returns a TypeError
#必填。要查找雙曲正切的數位。如果該值不是數位，則返回 TypeError
