#Python Keyword pass 關鍵字 pass
#A null statement, a statement that will do nothing
#null 語句，一條什麼都不做的語句。
#Create a placeholder for future code:
#為未來的代碼建立一個佔位元：
for x in [0, 1, 2]:
  pass
#Definition and Usage 定義和用法
#The pass statement is used as a placeholder for future code.
#pass 語句用作未來代碼的佔位元。
#When the pass statement is executed, nothing happens, but you avoid getting an error when empty code is not allowed.
#當執行 pass 語句時，什麼也不會發生，但是在不允許空代碼的情況下，可以避免出現錯誤。
#Empty code is not allowed in loops, function definitions, class definitions, or in if statements.
#迴圈、函數定義、類定義或 if 語句中不允許使用空代碼。
#Using the pass keyword in a function definition:
#在函數定義中使用pass 關鍵字：
def myfunction():
  pass
#Using the pass keyword in a class definition:
#在類別定義中使用 pass 關鍵字：
class Person:
  pass
#Using the pass keyword in an if statement:
#在 if 語句中使用 pass 關鍵字：
a = 33
b = 200
if b > a:
  pass
